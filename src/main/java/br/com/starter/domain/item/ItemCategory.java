package br.com.starter.domain.item;

public enum ItemCategory {
    ENGINE_PARTS,         // Peças do motor: pistões, válvulas, comando de válvulas, virabrequim
    TRANSMISSION,         // Sistema de transmissão: câmbio, embreagem, diferencial
    SUSPENSION,           // Suspensão e direção: amortecedores, molas, braços de controle
    BRAKES,               // Sistema de freios: pastilhas, discos, lonas, ABS
    ELECTRICAL,           // Elétrica e eletrônicos: alternador, bateria, chicote elétrico
    FILTERS,              // Filtros: óleo, ar, combustível, cabine
    LUBRICANTS,           // Óleos e lubrificantes: óleo do motor, fluido de transmissão, graxa
    EXHAUST_SYSTEM,       // Sistema de escape: catalisador, silenciador, coletor de escape
    COOLING_SYSTEM,       // Sistema de arrefecimento: radiador, bomba d'água, termostato
    TIRES_WHEELS,         // Pneus e rodas: pneus para carros, caminhões, tratores e rodas
    TOOLS,                // Ferramentas e equipamentos: chaves, scanners, elevadores
    ACCESSORIES,          // Acessórios: capas de banco, multimídia, frisos
    BODY_PARTS,           // Carroceria e estruturas: para-choques, portas, capô, chassi
    FUEL_SYSTEM,          // Sistema de combustível: bomba de combustível, injetores, carburador
    LIGHTING,             // Iluminação: faróis, lanternas, lâmpadas LED
    AIR_CONDITIONING,     // Ar-condicionado: compressor, condensador, filtro de cabine
    HYDRAULIC_SYSTEM,     // Sistema hidráulico: bombas, mangueiras, válvulas (tratores e caminhões)
    TRACTOR_SPECIFIC,     // Peças específicas para tratores: tomada de força, implementos agrícolas
    TRUCK_SPECIFIC,       // Peças específicas para caminhões: freios a ar, suspensão pneumática, tacógrafos
    MOTORCYCLE_SPECIFIC,  // Peças específicas para motos: kit relação, escapamento, guidão
    HEAVY_EQUIPMENT,      // Peças para máquinas pesadas: retroescavadeiras, escavadeiras, tratores de esteira
    ELECTRONIC_COMPONENTS,// Componentes eletrônicos: módulos de injeção, ECU, sensores avançados
    SAFETY_EQUIPMENT,     // Equipamentos de segurança: EPIs, cintos de segurança, airbags
    WORKSHOP_SUPPLIES;    // Materiais de consumo para oficina: solventes, panos, parafusos, EPIs
}
